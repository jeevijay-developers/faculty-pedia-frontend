// Utility function to filter classes by specialization
export const getClassesBySpecialization = (specialization) => {
  if (specialization === 'All') {
    return OneToOneLiveCourseData;
  }
  return OneToOneLiveCourseData.filter(classItem => classItem.specialization === specialization);
};

// Utility function to filter classes by subject
export const getClassesBySubject = (subject) => {
  if (subject === 'All') {
    return OneToOneLiveCourseData;
  }
  return OneToOneLiveCourseData.filter(classItem => classItem.subject === subject);
};